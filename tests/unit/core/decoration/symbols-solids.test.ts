const stubStringBounder: DriverStringBounder = {
  calculateDimension(font, text) {
    return { width: text.length * font.size * 0.5 };
  },
};

function newGraphic(): UGraphicSvg {
  return UGraphicSvg.build(0, basicSvgOption(), '$version$', stubStringBounder);
}

/** A `TextBlock` test double with a fixed, `stringBounder`-independent
 * dimension — same convention as `symbol-base.test.ts`'s `emptyTextBlock`. */
function fixedTextBlock(width: number, height: number): TextBlock {
  return {
    calculateDimension: () => new XDimension2D(width, height),
    drawU: () => {
      // no-op: this stub carries no drawable content.
    },
  };
}

interface Recorded {
  readonly dx: number;
  readonly dy: number;
}

/** A `TextBlock` test double that records the `UTranslate` its `drawU`
 * was invoked with, for asserting `mergeTB`'s per-block placement math. */
function recordingTextBlock(width: number, height: number, sink: Recorded[]): TextBlock {
  return {
    calculateDimension: () => new XDimension2D(width, height),
    drawU: (ug) => {
      sink.push({ dx: ug.getTranslate().getDx(), dy: ug.getTranslate().getDy() });
    },
  };
}

/** Exercises a file's exported `mergeTB` with LEFT/CENTER/RIGHT
 * alignment, asserting `dxForAlignment`'s three branches (private,
 * duplicated per-file per `USymbolDatabase.ts`'s doc comment) via the
 * translate each block's `drawU` observes. Top block: width 6, height 4.
 * Bottom block: width 10, height 5. Total width (mergeTB'd) = 10. */
function checkMergeTBBranches(mergeTB: typeof TextBlockUtils.mergeTB): void {
  const cases: readonly [HorizontalAlignment, number, number][] = [
    [HorizontalAlignment.LEFT, 0, 0],
    [HorizontalAlignment.CENTER, 2, 0],
    [HorizontalAlignment.RIGHT, 4, 0],
  ];
  for (const [alignment, topDx, bottomDxWhenNarrower] of cases) {
    const sink: Recorded[] = [];
    const top = recordingTextBlock(6, 4, sink);
    const bottom = recordingTextBlock(10, 5, sink);
    const tb = mergeTB(top, bottom, alignment);
    expect(tb.calculateDimension({} as never).getWidth()).toBe(10);
    expect(tb.calculateDimension({} as never).getHeight()).toBe(9);
    tb.drawU(newGraphic());
    expect(sink).toEqual([
      { dx: topDx, dy: 0 },
      { dx: bottomDxWhenNarrower, dy: 4 },
    ]);
  }
}

