/**
 * symbols-box.test.ts — T5: conformance + placement-math tests for the
 * box family (`USymbolRectangle`/`Card`/`Action`/`Label`/`Collections`/
 * `Stack`).
 *
 * Provenance for every real-jar fragment/number below:
 * `plantuml-1.2026.7beta3.jar -tsvg -pipe`, single-entity `.puml`
 * sources (`rectangle Foo` / `agent Foo` / `card Foo` / `action Foo` /
 * `label Foo` — each byte-verified to render at absolute origin
 * `(7,7)`, PlantUML's fixed content padding for a single, unlinked
 * DESCRIPTION-diagram entity) plus a `skinparam rectangleDiagonalCorner
 * 10` variant for the diagonal-corner case. `collections`/`stack` alone
 * (no link) misdetect as a SEQUENCE diagram (verified: jar's diagram-
 * type heuristic, not a port bug) — captured instead as `collections
 * Foo` / `stack Foo` alongside an unlinked `rectangle dummy` (forces
 * DESCRIPTION diagram type without introducing any layout-affecting
 * link), then their geometry re-derived onto the SAME `(7,7)` origin
 * every other case uses (arithmetic re-basing, not fabricated data —
 * every offset/radius/dimension below is read off real jar bytes; only
 * the coordinate origin is normalized so all six cases share one
 * reference frame). `"Foo"`'s real jar text metrics (`sans-serif`/14px):
 * width `24.7051`, height `16.4883` (label dimension = margin-subtracted
 * from jar's real overall box size, consistent across every case
 * below — cross-checked pairwise). Root `viewBox`/`width`/`height` for
 * each case is derived (not captured — no real jar diagram isolates a
 * bare symbol) from `SvgGraphicsCore#ensureVisible`'s documented,
 * already-conformant algorithm (`ensureVisible(x,y)`: `maxX =
 * max(maxX, trunc(x)+1)`, starting at `(10,10)`) applied to each case's
 * own real jar shape coordinates — never generated by running this
 * port's own emitter (verified independently via a throwaway Python
 * transcription of the same formula, not trusted "by construction").
 *
 * Text rendering itself is out of this task's scope (proven elsewhere,
 * Brief 1 T6's `emitter.golden.test.ts` suite) — every label/stereotype
 * `TextBlock` here is a lightweight double reporting the real jar
 * dimension above but drawing nothing (a `getTranslate()` spy instead),
 * so each conformance assertion below covers exactly this task's scope:
 * `SymbolContext` application, shape geometry/`deltaShadow`/corner
 * handling, and stereotype/label placement math (the `UTranslate` the
 * symbol hands the label/stereotype `TextBlock`, captured by the spy).
 */
