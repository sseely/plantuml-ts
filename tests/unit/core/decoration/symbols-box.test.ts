const FOO_WIDTH = 24.7051;
const FOO_HEIGHT = 16.4883;

const EMPTY_STEREOTYPE: TextBlock = {
  calculateDimension: () => new XDimension2D(0, 0),
  drawU: () => {
    // Intentionally empty — represents an absent stereotype (0x0, no-op
    // draw), matching how EntityImageDescription passes an empty
    // TextBlock when no stereotype is present.
  },
};

/** A spy `TextBlock` reporting a fixed `(width, height)` dimension and
 * recording the `UTranslate` it was drawn with, WITHOUT drawing
 * anything visible — text-rendering fidelity is out of this task's
 * scope (see module doc comment). Used for label/title/stereotype
 * doubles alike. */
function spyBlock(width: number, height: number): { tb: TextBlock; translate(): UTranslate | undefined } {
  let captured: UTranslate | undefined;
  return {
    tb: {
      calculateDimension: () => new XDimension2D(width, height),
      drawU: (ug: UGraphic) => {
        captured = ug.getTranslate();
      },
    },
    translate: () => captured,
  };
}

/** `Foo`'s real jar-measured dimension (see the module doc comment for
 * provenance) — the label double every `asSmall` conformance test
 * above uses. */
function spyLabel(): { tb: TextBlock; translate(): UTranslate | undefined } {
  return spyBlock(FOO_WIDTH, FOO_HEIGHT);
}

const STUB_STRING_BOUNDER = { calculateDimension: () => ({ width: 0 }) };

function newRoot(): UGraphicSvg {
  return UGraphicSvg.build(0, basicSvgOption(), '$version$', STUB_STRING_BOUNDER);
}

/** Every real-jar single-entity capture this suite conforms against
 * renders at absolute origin `(7,7)` (PlantUML's fixed content padding
 * for an unlinked DESCRIPTION-diagram entity) — see module doc comment. */
const ORIGIN = new UTranslate(7, 7);

function wrapDocument(rootAttrs: string, body: string): string {
  return (
    `<svg xmlns="http://www.w3.org/2000/svg" xmlns:xlink="http://www.w3.org/1999/xlink" version="1.1" ${rootAttrs}` +
    ` zoomAndPan="magnify" preserveAspectRatio="none" contentStyleType="text/css">` +
    `<?plantuml $version$?><defs/><g>${body}</g></svg>`
  );
}

/** Default `SymbolContext` every box-family conformance case below uses:
 * `backColor=#F1F1F1`, `foreColor=#181818`, `stroke=0.5pt`, no shadow,
 * `roundCorner=5` (halved to `rx=ry=2.5` at serialization — see
 * `URectangle.ts`'s own doc comment), no diagonal corner. Matches every
 * real jar capture's own `fill`/`style`/`rx`/`ry` attributes exactly. */
function defaultContext(): SymbolContext {
  return new SymbolContext('#F1F1F1', '#181818', UStroke.withThickness(0.5), 0, 5, 0);
}

