describe('USymbolRectangle', () => {
  const GOLDEN = wrapDocument(
    'style="width:52px;height:44px;" width="52px" height="44px" viewBox="0 0 52 44"',
    '<rect x="7" y="7" width="44.7051" height="36.4883" fill="#F1F1F1" style="stroke:#181818;stroke-width:0.5;" rx="2.5" ry="2.5"/>',
  );

  test('asSmall (rectangle): conformant vs jar fragment (rectangle Foo)', () => {
    const symbol = new USymbolRectangle('rectangle');
    const label = spyLabel();
    const root = newRoot();
    symbol.asSmall(EMPTY_STEREOTYPE, label.tb, EMPTY_STEREOTYPE, defaultContext(), HorizontalAlignment.CENTER).drawU(root.apply(ORIGIN));
    const { pass, diffs } = compareSvg(root.getSvgString(), GOLDEN, 'deterministic');
    expect(pass, JSON.stringify(diffs[0])).toBe(true);
  });

  test('agent maps to USymbolRectangle(SName.agent) — byte-identical to the rectangle variant', () => {
    // Verified against USymbols.java:65 — `AGENT = new
    // USymbolRectangle(SName.agent)` — and against real jar output
    // (`agent Foo` vs `rectangle Foo`): identical <rect>/<text>
    // fragments (see module doc comment / this file's write-up in the
    // task report).
    const agentRoot = newRoot();
    new USymbolRectangle('agent')
      .asSmall(EMPTY_STEREOTYPE, spyLabel().tb, EMPTY_STEREOTYPE, defaultContext(), HorizontalAlignment.CENTER)
      .drawU(agentRoot.apply(ORIGIN));

    const rectRoot = newRoot();
    new USymbolRectangle('rectangle')
      .asSmall(EMPTY_STEREOTYPE, spyLabel().tb, EMPTY_STEREOTYPE, defaultContext(), HorizontalAlignment.CENTER)
      .drawU(rectRoot.apply(ORIGIN));

    expect(agentRoot.getSvgString()).toBe(rectRoot.getSvgString());
  });

  test('asSmall: roundCorner=5 halves to rx=ry=2.5 (URectangle.rounded semantics)', () => {
    const root = newRoot();
    new USymbolRectangle('rectangle')
      .asSmall(EMPTY_STEREOTYPE, spyLabel().tb, EMPTY_STEREOTYPE, defaultContext(), HorizontalAlignment.CENTER)
      .drawU(root.apply(ORIGIN));
    expect(root.getSvgString()).toContain('rx="2.5"');
    expect(root.getSvgString()).toContain('ry="2.5"');
  });

  test('asSmall: diagonalCorner=10 draws a UPath outline, conformant vs jar fragment (skinparam rectangleDiagonalCorner 10)', () => {
    const GOLDEN_DIAGONAL = wrapDocument(
      'style="width:52px;height:44px;" width="52px" height="44px" viewBox="0 0 52 44"',
      '<path d="M17,7 L41.7051,7 L51.7051,17 L51.7051,33.4883 L41.7051,43.4883 L17,43.4883 L7,33.4883 L7,17 L17,7" ' +
        'style="stroke:#181818;stroke-width:0.5;" fill="#F1F1F1"/>',
    );
    const diagonalContext = new SymbolContext('#F1F1F1', '#181818', UStroke.withThickness(0.5), 0, 0, 10);
    const root = newRoot();
    new USymbolRectangle('rectangle')
      .asSmall(EMPTY_STEREOTYPE, spyLabel().tb, EMPTY_STEREOTYPE, diagonalContext, HorizontalAlignment.CENTER)
      .drawU(root.apply(ORIGIN));
    const { pass, diffs } = compareSvg(root.getSvgString(), GOLDEN_DIAGONAL, 'deterministic');
    expect(pass, JSON.stringify(diffs[0])).toBe(true);
  });

  test('asSmall: label placement translate matches margin (10,10)', () => {
    const label = spyLabel();
    const root = newRoot();
    new USymbolRectangle('rectangle')
      .asSmall(EMPTY_STEREOTYPE, label.tb, EMPTY_STEREOTYPE, defaultContext(), HorizontalAlignment.CENTER)
      .drawU(root.apply(ORIGIN));
    const translate = label.translate();
    expect(translate?.getDx()).toBe(7 + 10);
    expect(translate?.getDy()).toBe(7 + 10);
  });
});

