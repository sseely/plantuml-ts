#!/usr/bin/env node
/**
 * Converts the JSON emitted by ExtractJarFontMetrics.java (on stdin) into
 * src/core/measurer-jar.data.ts (on stdout).
 *
 * Usage: see README.md in this directory for the full regeneration command.
 *
 * Plain Node/ESM (not TypeScript) on purpose: this is dev tooling that runs
 * once per regeneration, not part of the library. Keeping it out of
 * tsconfig.node.json's `scripts/**\/*.ts` glob and out of `npm run lint`'s
 * `src tests demo` target avoids adding an unnecessary quality-gate surface
 * for a one-shot generator.
 */
import { readFileSync } from 'node:fs';

const MIN_CODEPOINT = 32;
const MAX_CODEPOINT = 591;
const VALUES_PER_LINE = 10;

function readStdinJson() {
  const raw = readFileSync(0, 'utf8'); // fd 0 = stdin
  return JSON.parse(raw);
}

function formatNumber(x) {
  if (x === 0) return '0';
  const s = x.toFixed(9).replace(/0+$/, '').replace(/\.$/, '');
  return s === '' ? '0' : s;
}

function buildAdvanceRows(advancesAtReference, referenceSize) {
  const perPoint = [];
  for (let cp = MIN_CODEPOINT; cp <= MAX_CODEPOINT; cp++) {
    const raw = advancesAtReference[String(cp)];
    if (raw === undefined) {
      throw new Error(`missing advance for codepoint ${cp} in extractor output`);
    }
    perPoint.push(raw / referenceSize);
  }

  const rows = [];
  for (let i = 0; i < perPoint.length; i += VALUES_PER_LINE) {
    const chunk = perPoint.slice(i, i + VALUES_PER_LINE);
    rows.push('  ' + chunk.map(formatNumber).join(', ') + ',');
  }
  return { rows, perPoint };
}

function renderFile(data) {
  const { referenceSize, ascentAtReference, descentAtReference, averageAdvanceAtReference } = data;
  const { rows } = buildAdvanceRows(data.advancesAtReference, referenceSize);
  const ascentPerPoint = ascentAtReference / referenceSize;
  const descentPerPoint = descentAtReference / referenceSize;
  const fallbackAdvance = averageAdvanceAtReference / referenceSize;

  const wSample = data.advancesAtReference['87'] / referenceSize;
  const spaceSample = data.advancesAtReference['32'] / referenceSize;

  return `// AUTO-GENERATED by scripts/extract-jar-font-metrics — regenerate with
// the command documented in scripts/extract-jar-font-metrics/README.md.
// Do not edit by hand.
//
// Extracted from OpenJDK ${data.jvmVersion} (${data.jvmVendor}) on
// ${data.osName}, from java.awt.Font.decode("SansSerif") — the exact
// logical font family PlantUML's FontStack.SANS_SERIF resolves via
// Font.decode(name) (see klimt/font/FontStack.java, FontParam.FAMILY).
// Font.getFontName() reported "${data.resolvedFontName}" on the extraction
// JVM; the physical face behind the "SansSerif" logical name is resolved by
// the platform's font configuration, which is exactly why this table must
// be regenerated per-JVM/per-platform rather than hand-maintained.
//
// All values are PER-POINT (i.e. measured at a reference font size of
// ${referenceSize}, then divided by ${referenceSize}) so callers multiply
// directly by the desired font size — no separate reference-size field is
// needed, matching the JarFontMetrics contract exactly.
//
// Measurement path mirrors PlantUML's SVG string bounders exactly:
// FileFormat.getJavaDimension(UFont, String) calls
// FontMetrics.getStringBounds(text, gg) against a Graphics2D with
// TEXT_ANTIALIASING + FRACTIONALMETRICS both ON (see FileFormat.java,
// StringBounderSvg.java) — fractional (non-rounded) advances, NOT
// FontMetrics.charWidth(int)'s rounded ints. See
// scripts/extract-jar-font-metrics/README.md for the spot-check evidence.
//
// Verified during extraction (see README.md for the full JSON):
//  - advance(size) == advance(1) * size to double-precision epsilon
//    (~1e-15) for every glyph/size pair tested — confirms per-point
//    normalization reproduces every font size exactly.
//  - sum(perGlyphAdvance) == getStringBounds(wholeString) (delta 0.0) for
//    "Hello World", "AVA", "Type" at 14pt — confirms AWT applies no kerning
//    for this font, so a per-glyph table is additive-safe for arbitrary
//    strings.
//  - 'W' (cp 87) @ 1pt = ${formatNumber(wSample)}; ' ' (cp 32) @ 1pt =
//    ${formatNumber(spaceSample)}. At 14pt: W = ${formatNumber(wSample * 14)}px,
//    space = ${formatNumber(spaceSample * 14)}px — matches
//    FontMetrics.getStringBounds at 14pt on the extraction JVM (see
//    README.md "Spot-check values").

/** Per-point (font size == 1) jar-measured font metrics, keyed by codepoint. */
export interface JarFontMetrics {
  family: string;
  ascent: number;
  descent: number;
  /** codepoint -> per-point advance width (multiply by font size to use). */
  advances: Readonly<Record<number, number>>;
  /** Per-point fallback advance for codepoints outside \`advances\`
   *  (arithmetic mean of every measured advance in this table). */
  fallbackAdvance: number;
}

const MIN_CODEPOINT = ${MIN_CODEPOINT};

// Per-point AWT advance widths for codepoints ${MIN_CODEPOINT}-${MAX_CODEPOINT}
// inclusive (Basic Latin + Latin-1 Supplement + Latin Extended-A), indexed
// by (codepoint - MIN_CODEPOINT). ${VALUES_PER_LINE} values per line.
const ADVANCES_PER_POINT: readonly number[] = [
${rows.join('\n')}
];

function buildAdvances(): Readonly<Record<number, number>> {
  const result: Record<number, number> = {};
  for (let i = 0; i < ADVANCES_PER_POINT.length; i++) {
    result[MIN_CODEPOINT + i] = ADVANCES_PER_POINT[i]!;
  }
  return result;
}

/** Jar-measured metrics for PlantUML's default "SansSerif" description-diagram
 *  font. See the file header for extraction provenance and verification. */
export const JAR_SANS_SERIF_METRICS: JarFontMetrics = {
  family: 'SansSerif',
  ascent: ${formatNumber(ascentPerPoint)},
  descent: ${formatNumber(descentPerPoint)},
  advances: buildAdvances(),
  fallbackAdvance: ${formatNumber(fallbackAdvance)},
};
`;
}

const data = readStdinJson();
process.stdout.write(renderFile(data));
